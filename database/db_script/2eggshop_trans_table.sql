--shopping cart
DROP TABLE IF EXISTS ES_SHOPPING_CART;
CREATE TABLE ES_SHOPPING_CART(
    UNIQUE_CODE UUID PRIMARY KEY,--id
    SELECTED BOOLEAN DEFAULT TRUE,--是否选中
    USER_UC UUID,--用户id
    PRODUCT_UC UUID,--产品id
    PRODUCT_COUNT DECIMAL(20, 2),--产品数量
    REMARK VARCHAR,--备注
    CREATOR VARCHAR,
    CREATE_TIME TIMESTAMP DEFAULT NOW()
);

