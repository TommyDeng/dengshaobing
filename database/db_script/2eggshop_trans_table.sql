--order 
DROP TABLE IF EXISTS ES_ORDER;
CREATE TABLE ES_ORDER(
    UNIQUE_CODE UUID PRIMARY KEY,--order id
    USER_UC UUID,--用户id
    SUMMARY VARCHAR,--描述、简述
    TOTAL_AMT DECIMAL(20, 2),--总金额
    RECEIVER_NAME VARCHAR,--收件人
   	PROVINCE VARCHAR,--省份
    CITY VARCHAR,--城市
    COUNTRY VARCHAR,--区
    TOWN VARCHAR,--乡镇
    ADDRESS VARCHAR,--详细地址
    CONTACT VARCHAR,--联系方式
    PAYMENT_TYPE VARCHAR,--付款方式
    STATUS VARCHAR,--订单状态
    REMARK VARCHAR,--备注
    PAYMENT_UC UUID,--付款id
    PAY_MESSAGE VARCHAR,--付款消息（成功或者错误消息）
    PAY_SUCC_TIME TIMESTAMP,--付款成功时间
    CREATOR VARCHAR,
    CREATE_TIME TIMESTAMP DEFAULT NOW()
);
--order item
DROP TABLE IF EXISTS ES_ORDER_ITEM;
CREATE TABLE ES_ORDER_ITEM(
    ORDER_UC UUID,--orderid
    PRODUCT_UC UUID,--商品id
    PRODUCT_COUNT DECIMAL(20, 2),--商品数量
    PRODUCT_PRICE DECIMAL(20, 2),--商品价格
    SUB_AMT DECIMAL(20, 2),--item小计
    CREATOR VARCHAR,
    CREATE_TIME TIMESTAMP DEFAULT NOW()
);

--product
DROP TABLE IF EXISTS ES_PRODUCT;
CREATE TABLE ES_PRODUCT(
    UNIQUE_CODE UUID PRIMARY KEY,--商品id
    NAME VARCHAR,--商品名称
    PRICE DECIMAL(20, 2),--价格
    CATEGORY UUID,--类别
    THUMBNAIL UUID,--缩略图file id
    BRIEF VARCHAR,--简述
    DETAIL_DESCIPTION VARCHAR(5000),--详细描述html
    CREATOR VARCHAR,
    CREATE_TIME TIMESTAMP DEFAULT NOW()
);

--product media
DROP TABLE IF EXISTS ES_PRODUCT_MEDIA;
CREATE TABLE ES_PRODUCT_MEDIA(
    UNIQUE_CODE UUID PRIMARY KEY,--id
    PRODUCT_UC UUID,--产品id
    ORDER_NUM INT,--顺序
    THUMBNAIL UUID,--file id
    MEDIA_TYPE VARCHAR,--类别
    CREATOR VARCHAR,
    CREATE_TIME TIMESTAMP DEFAULT NOW()
